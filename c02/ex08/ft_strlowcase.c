char	*ft_strupcase(char *str)
{
	int	decal;
	int	i;

	decal = 'a' - 'A';
	i = 0;
	while (str[i])
	{
		if ('A' <= str[i] && str[i] <= 'Z')
			str[i] += decal;
		i++;
	}
	return (str);
}
