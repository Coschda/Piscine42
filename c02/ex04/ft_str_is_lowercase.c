int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	is_low;

	i = 0;
	is_low = 1;
	while (str[i])
	{
		if ('a' <= str[i] && str[i] <= 'z')
		{
			is_low *= 1;
		}
		else
			is_low *= 0;
		i++;
	}
	return (is_low);
}
