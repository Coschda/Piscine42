int	ft_str_is_numeric(char *str)
{
	int	i;
	int	is_num;

	i = 0;
	is_num = 1;
	while (str[i])
	{
		if ('0' <= str[i] && str[i] <= '9')
		{
			is_num *= 1;
		}
		else
			is_num *= 0;
		i++;
	}
	return (is_num);
}
